class Calculadora {

    static sumar(num1, num2) {
        return num1 + num2
    }

    static restar(num1, num2) {
        return num1 - num2
    }

    static multiplicar(num1, num2) {
        return num1 * num2
    }

    static dividir(num1, num2) {
        return num1 / num2
    }
}

module.exports = Calculadora;