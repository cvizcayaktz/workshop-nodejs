class Calculadora {

    static sumar(param1, param2) {
        return param1 + param2;
    }

    static restar(param1, param2) {
        return param1 - param2;
    }

    static multiplicar(param1, param2) {
        return param1 * param2;
    }

    static dividir(param1, param2) {
        return param1 / param2;
    }
}

module.exports = Calculadora;