class CalculadoraStatic {
    
    static sumar (x1, x2) {
        return x1 + x2;
    }

    static restar (x1, x2) {
        return x1 - x2;
    }

    static multiplicar (x1, x2) {
        return x1 * x2;
    }

    static dividir (x1, x2) {
        return x1 / x2;
    }
}

module.exports = CalculadoraStatic